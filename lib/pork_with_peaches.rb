# encoding: utf-8

class PorkWithPeaches
  def self.recipe
    puts """
      Zutaten:
        - 600g Schweinefilet
        - 1 große Dose Pfirsiche (halbe Frucht)
        - 1 Flasche BBQ Sauce
        - Sojasauce
        - 1 Packung Sahne
        - 1 EL Butter
        - Salz, Pfeffer
        - Thymian, Majoran, Currypulver
        - Öl
        
        
      Schweinefilet in etwa 5 cm dicke Scheiben schneiden und in etwas Öl anbraten.
      Anschließend in eine Auflaufform legen.
      
      BBQ Sauce Saft der Pfirsiche, Sojasauce, Sahne und Gewürze mischen und über das Fleisch geben.
      
      Auflaufform bei 180°C (Umluft) für 30 Minuten im Ofen Backen
      
      Pfirsichhälten in Butter anbraten bis die 'Farbe' bekommen.
      
      Fleich mit Pfirsichen Sauce und Reis Servieren.
      
    """
  end
end